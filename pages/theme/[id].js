export async function getServerSideProps(context) {
  let cookies = nookies.get(context).user_token;

  if (!cookies) {
    return {
      redirect: {
        destination: '/login',
        permanent: false,
      },
    };
  }

  const res = await fetch(
    `https://api.dev.citizenwave.me/api/v1/admin/themes/${context.params.id}`,
    {
      headers: {
        Authorization: `Bearer ${cookies}`,
      },
    }
  );

  const theme = await res.json();

  if (!theme) {
    return {
      notFound: true,
    };
  }

  return { props: { theme } };
}
